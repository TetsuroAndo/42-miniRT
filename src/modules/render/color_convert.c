#include "mod_render.h"

/**
 * @brief 整数形式の色値（TRGB）を浮動小数点形式（t_rgbd）に変換する関数
 * 
 * @param c 変換する整数形式の色値（TRGB形式の32ビット整数）
 * @return t_rgbd 変換後の浮動小数点形式の色値（各成分は0.0～1.0の範囲）
 * 
 * この関数は、MinilibXなどで使用される整数形式の色値（TRGB）を
 * レイトレーシング計算で使用する浮動小数点形式（t_rgbd）に変換します。
 * 各色成分（R, G, B）をビットシフトとマスクで抽出し、0～255の範囲から0.0～1.0の範囲に正規化します。
 * 透明度（T）は無視されます。
 */
t_rgbd	trgb_to_rgbd(int c)
{
	return ((t_rgbd){.r = ((c >> 16) & 0xFF) / 255.0,
		.g = ((c >> 8) & 0xFF) / 255.0,
		.b = (c & 0xFF) / 255.0});
}

/**
 * @brief 浮動小数点形式の色値（t_rgbd）を整数形式（TRGB）に変換する関数
 * 
 * @param v 変換する浮動小数点形式の色値（各成分は0.0～1.0の範囲を想定）
 * @return int 変換後の整数形式の色値（TRGB形式の32ビット整数）
 * 
 * この関数は、レイトレーシング計算で使用する浮動小数点形式（t_rgbd）の色値を
 * MinilibXなどで使用される整数形式（TRGB）に変換します。
 * 各色成分（R, G, B）を0.0～1.0の範囲から0～255の範囲に変換し、四捨五入で丸めます。
 * 1.0を超える値は1.0にクランプされます。透明度（T）は0に設定されます。
 */
int	rgbd_to_trgb(t_rgbd v)
{
	return (create_trgb(
			0,
			(int)(fmin(v.r, 1.0) * 255.0 + 0.5),
		(int)(fmin(v.g, 1.0) * 255.0 + 0.5),
			(int)(fmin(v.b, 1.0) * 255.0 + 0.5)));
}

/**
 * @brief 浮動小数点形式の色同士を加算する関数
 * 
 * @param a 加算する1つ目の色
 * @param b 加算する2つ目の色
 * @return t_rgbd 加算結果の色
 * 
 * この関数は、二つの浮動小数点形式の色値（t_rgbd）を成分ごとに加算します。
 * 主にレイトレーシングにおいて、ローカル照明と反射光など、複数の光の寄与を
 * 合成する際に使用されます。
 * 結果はクランプされないため、1.0を超える値が生成される可能性があります。
 * 最終的な表示前にはトーンマッピングやクランプ処理が必要です。
 */
t_rgbd	rgbd_add(t_rgbd a, t_rgbd b)
{
	return ((t_rgbd){a.r + b.r, a.g + b.g, a.b + b.b});
}

/**
 * @brief 浮動小数点形式の色をスケーリング（倍率変更）する関数
 * 
 * @param a スケーリングする色
 * @param s スケール係数（倍率）
 * @return t_rgbd スケーリング後の色
 * 
 * この関数は、浮動小数点形式の色値（t_rgbd）の各成分を指定された係数で掛け算します。
 * 主にレイトレーシングにおいて、反射率に基づく色の調整や光の強度を制御する際に
 * 使用されます。
 * 例えば、反射率が0.3のオブジェクトの場合、ローカル色を0.7倍、反射色を0.3倍して
 * 合成するといった使い方をします。
 */
t_rgbd	rgbd_scale(t_rgbd a, double s)
{
	return ((t_rgbd){a.r * s, a.g * s, a.b * s});
}

/**
 * @brief 個別の色成分からTRGB形式の32ビット整数色値を生成する関数
 * 
 * @param t 透明度成分（0～255）
 * @param r 赤成分（0～255）
 * @param g 緑成分（0～255）
 * @param b 青成分（0～255）
 * @return int 生成されたTRGB形式の32ビット整数色値
 * 
 * この関数は、個別の色成分（T, R, G, B）からMinilibXで使用される
 * 32ビット整数形式の色値を生成します。
 * 各成分は0～255の範囲で指定され、範囲を超える値は255にクランプされます。
 * 各成分は適切なビット位置にシフトされ、ビット単位のOR演算で結合されます。
 * 
 * 具体的なビット配置：
 * - T（透明度）：ビット24-31
 * - R（赤）：ビット16-23
 * - G（緑）：ビット8-15
 * - B（青）：ビット0-7
 */
int	create_trgb(int t, int r, int g, int b)
{
	if (255 < r)
		r = 255;
	if (255 < g)
		g = 255;
	if (255 < b)
		b = 255;
	return (t << 24 | r << 16 | g << 8 | b);
}
